package com.extole.consumer.rest.impl.redirect;

public class LinkFollowingRuntimeException extends RuntimeException {

    public LinkFollowingRuntimeException() {
        super();
    }

    public LinkFollowingRuntimeException(String message) {
        super(message);
    }

    public LinkFollowingRuntimeException(Throwable cause) {
        super(cause);
    }

    public LinkFollowingRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }

}
